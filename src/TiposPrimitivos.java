public class TiposPrimitivos{
    public static void main(String[] args){
        byte b = 10; // 1 byte = 8 bits
        short s = 20; // 2 bytes = 16 bits
        int i = 3; // 4 bytes = 32 bits
        long l = 4; // 8 bytes = 64 bits

        float f = 1.0f; // Ponto Flutuante de 4 bytes
        double d = 2.0; // Ponto Flutuante de 8 bytes

        char c = 'a'; // 2 bytes
        boolean bb = true; // 1 byte
    }
}